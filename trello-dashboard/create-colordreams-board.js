async function addCardsToBoard() {
    try {
        console.log('===============================================');
        console.log('Color Dreams - Agregando tareas al board');
        console.log('===============================================\n');

        const boardsResponse = await fetch(
            `${TRELLO_API_BASE}/members/me/boards?key=${TRELLO_API_KEY}&token=${TRELLO_TOKEN}`
        );
        const boards = await boardsResponse.json();
        const board = boards.find(b => b.name === BOARD_NAME && !b.closed);

        if (!board) {
            console.log(`Board "${BOARD_NAME}" no encontrado.`);
            return;
        }

        console.log(`Board encontrado: ${BOARD_NAME} (${board.id})\n`);

        const listsResponse = await fetch(
            `${TRELLO_API_BASE}/boards/${board.id}/lists?key=${TRELLO_API_KEY}&token=${TRELLO_TOKEN}`
        );
        const lists = await listsResponse.json();

        let targetList = lists.find(l => l.name === 'En construcción');

        if (!targetList) {
            console.log('Lista "En construcción" no encontrada, creándola...\n');
            const createListResponse = await fetch(
                `${TRELLO_API_BASE}/lists?key=${TRELLO_API_KEY}&token=${TRELLO_TOKEN}`,
                {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify({
                        name: 'En construcción',
                        idBoard: board.id,
                        pos: 'top'
                    })
                }
            );
            targetList = await createListResponse.json();
        }

        console.log(`Lista "En construcción" lista: ${targetList.id}\n`);
        console.log('Creando tarjetas...\n');

        let created = 0;
        for (const cardData of COLORDREAMS_CARDS) {
            const cardResponse = await fetch(
                `${TRELLO_API_BASE}/cards?key=${TRELLO_API_KEY}&token=${TRELLO_TOKEN}`,
                {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify({
                        name: cardData.name,
                        desc: cardData.desc,
                        idList: targetList.id,
                        pos: 'bottom'
                    })
                }
            );

            if (cardResponse.ok) {
                console.log(`  Creada: ${cardData.name}`);
                created++;
            } else {
                const error = await cardResponse.text();
                console.log(`  Error: ${cardData.name} - ${error}`);
            }

            await new Promise(resolve => setTimeout(resolve, 150));
        }

        console.log(`\n${created} tarjetas creadas!\n`);
        console.log('===============================================');
        console.log('PROCESO COMPLETADO');
        console.log('===============================================');
        console.log(`\nBoard: ${BOARD_NAME}`);
        console.log(`URL: ${board.url || board.shortUrl}`);
        console.log(`Tarjetas: ${COLORDREAMS_CARDS.length}\n`);

    } catch (error) {
        console.error('\nERROR:', error.message);
    }
}

addCardsToBoard();
